// ============ SCRIPT FORMATTER ============
function formatScriptContent(rawText) {
  const lines = rawText.split('\n');
  let html = '';
  let inScriptSection = false;
  let currentSection = '';
  
  // Known character names for dialogue detection
  const characters = ['OMAR', 'MOKTHAR', 'MOKHTAR', 'MOKTAHR', 'SIDI', 'ÀLVAR', 'JOSE', 'NÚRIA', 'AMIR', 'RAMÓN', 'CHAFI', 'JAVIER', 'GUERRILLERO', 'MÉDICO TUAREG', 'COORDINADOR', 'COMENTARISTA', 'LÍDER'];
  
  // Section headers in the analysis
  const sectionHeaders = [
    'Lo que necesito antes de grabar',
    'Lo que voy a usar en acción / corte',
    'Secuencia de guión',
    'Secuencia del guion'
  ];
  
  // Field labels
  const fieldLabels = [
    'Qué ocurre objetivamente',
    'Función dramática de Omar',
    'Motivación general en relación con la secuencia',
    'Relación de fuerzas',
    'Acción o intención jugable',
    'Riesgo a evitar como actor',
    'Otra información útil del mapa',
    'Specificum útil',
    'Specificum util',
    'Llave maestra',
    'Fórmula general útil de la secuencia',
    'Idea resumen para mí',
    'Gráfico de conflicto',
    'Escala de intensidad',
    '¿Quién soy\\? = Característica',
    '¿Qué quiero\\? = Objetivo',
    '¿Qué factores rodean el hecho\\? = Circunstancia',
    '¿Qué factors rodean el hecho\\? = Circunstancia',
    '¿Cómo lo hago\\? = Actitud',
    '¿Qué hago concretamente\\? = Actitud',
    'Interior',
    'Exterior',
    'Interna',
    'Externa'
  ];
  
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) {
      html += '<div class="script-line-break"></div>';
      continue;
    }
    
    // Title line (OMAR — X.XX)
    if (i === 0 && (line.startsWith('OMAR') || line.match(/^\d+\.\d+$/))) {
      html += `<div class="script-title">${escapeHtml(line)}</div>`;
      continue;
    }
    
    // Estado line
    if (line.startsWith('Estado:')) {
      const isDone = line.includes('cerrado') || line.includes('✅');
      html += `<div class="script-estado ${isDone ? 'done' : 'wip'}">${escapeHtml(line)}</div>`;
      continue;
    }
    
    // Section headers
    let isSectionHeader = false;
    for (const sh of sectionHeaders) {
      if (line === sh || line.startsWith(sh)) {
        const isScript = sh.startsWith('Secuencia');
        inScriptSection = isScript;
        html += `<div class="script-section-header ${isScript ? 'screenplay' : ''}">${escapeHtml(line)}</div>`;
        isSectionHeader = true;
        break;
      }
    }
    if (isSectionHeader) continue;
    
    // Stage direction (EXT. / INT.)
    if (line.match(/^(EXT\.|INT\.|EXT\/INT\.)/)) {
      inScriptSection = true;
      html += `<div class="script-stage-direction">${escapeHtml(line)}</div>`;
      continue;
    }
    
    // Dialogue detection
    if (inScriptSection) {
      let isDialogue = false;
      for (const char of characters) {
        if (line.startsWith(char + ' ') || line.startsWith(char + '\n') || line === char || line.match(new RegExp('^' + char + '\\s*\\('))) {
          const charName = char;
          const rest = line.substring(char.length).trim();
          const parenthetical = rest.match(/^(\([^)]*\))\s*(.*)/);
          if (parenthetical) {
            html += `<div class="script-dialogue"><span class="script-char-name">${escapeHtml(charName)}</span> <span class="script-parenthetical">${escapeHtml(parenthetical[1])}</span></div>`;
            if (parenthetical[2]) {
              html += `<div class="script-dialogue-text">${escapeHtml(parenthetical[2])}</div>`;
            }
          } else if (rest) {
            html += `<div class="script-dialogue"><span class="script-char-name">${escapeHtml(charName)}</span></div>`;
            html += `<div class="script-dialogue-text">${escapeHtml(rest)}</div>`;
          } else {
            html += `<div class="script-dialogue"><span class="script-char-name">${escapeHtml(charName)}</span></div>`;
          }
          isDialogue = true;
          break;
        }
      }
      if (isDialogue) continue;
      
      // Parenthetical on its own line in script
      if (line.startsWith('(') && line.endsWith(')')) {
        html += `<div class="script-dialogue-text"><span class="script-parenthetical">${escapeHtml(line)}</span></div>`;
        continue;
      }
      
      // Action lines in script section
      html += `<div class="script-action">${escapeHtml(line)}</div>`;
      continue;
    }
    
    // Field labels in analysis
    let isField = false;
    for (const fl of fieldLabels) {
      const regex = new RegExp('^' + fl);
      if (regex.test(line)) {
        const labelMatch = line.match(new RegExp('^(' + fl.replace(/\\\\/g, '') + ')\\s*(.*)'));
        if (labelMatch) {
          const cleanLabel = fl.replace(/\\\\/g, '');
          const value = line.substring(cleanLabel.length).trim();
          html += `<div class="script-field"><span class="script-field-label">${escapeHtml(cleanLabel)}</span> ${value ? escapeHtml(value) : ''}</div>`;
        } else {
          html += `<div class="script-field"><span class="script-field-label">${escapeHtml(line)}</span></div>`;
        }
        isField = true;
        break;
      }
    }
    if (isField) continue;
    
    // Intensity numbers (e.g. "5 → 4 → 3")
    if (line.match(/^\d+\s*[→→]/) || line.match(/^\d+:\s/)) {
      html += `<div class="script-intensity">${escapeHtml(line)}</div>`;
      continue;
    }
    
    // Default
    html += `<div class="script-body-line">${escapeHtml(line)}</div>`;
  }
  
  return html;
}

// ============ STATS ============
function computeStats() {
  let totalSeqs = 0;
  let doneSeqs = 0;
  
  SCHEDULE.forEach(day => {
    day.sequences.forEach(seq => {
      totalSeqs++;
      if (getSeqStatus(seq.id) === STATUS.DONE) doneSeqs++;
    });
  });
  
  return { totalDays: SCHEDULE.length, totalSeqs, doneSeqs };
}

function getDayStats(dayIndex) {
  const day = SCHEDULE[dayIndex];
  let done = 0;
  day.sequences.forEach(seq => {
    if (getSeqStatus(seq.id) === STATUS.DONE) done++;
  });
  return { total: day.sequences.length, done };
}

